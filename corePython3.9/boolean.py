# The boolean value has a technical name as bool.
# The boolean data type has two values: True and False.
# Note that the boolean values True and False start with the capital letters (T) and (F).

is_active = True
print(is_active)

# The bool() function

value = bool("Hi")
print(value)

value = bool('')
print(value)
value = bool(100)
print(value)
value = bool(0)
print(value)

