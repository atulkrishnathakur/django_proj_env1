# type() function used to get type of variable or value
data1 = "1000"
print(type(data1))

# string to int conversion
data2 = "200"
convertedData = int(data2)
print(type(convertedData))

# string to float conversion
data2 = "200"
print(data2)
convertedData = float(data2)
print(type(convertedData))

# int to float
data2 = 600
convertedData = float(data2)
print(convertedData)
print(type(convertedData))

# float to int
data2 = 500.8956
convertedData = int(data2)
print(convertedData)
print(type(convertedData))

# int to string
data2 = 650
convertedData = str(data2)
print(convertedData)
print(type(convertedData))

# float to string
data2 = 850.52
convertedData = str(data2)
print(convertedData)
print(type(convertedData))

# string to bool
data2 = "True"
convertedData = bool(data2)
print(convertedData)
print(type(convertedData))

